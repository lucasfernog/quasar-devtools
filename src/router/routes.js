
const routes = [
  {
    path: '/',
    component: () => import('layouts/MyLayout.vue'),
    children: [
      {
        path: '',
        component: () => import('pages/Index.vue')
      },
      {
        path: 'devtools',
        component: () => import('pages/DevTools.vue')
      },
      {
        path: 'devtools-panel',
        component: () => import('pages/DevToolsPanel.vue')
      }
    ]
  }
]

